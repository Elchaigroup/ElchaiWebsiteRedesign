#!/usr/bin/env node
// One-shot splitter for src/lib/service-detail-content.ts.
// Reads the original, extracts each `const <name>: ServiceDetailContent = { … };`
// block into a category file under src/lib/services/, and writes a thin index
// that re-exposes the same public surface.
//
// Idempotent: re-running re-derives every output file from the original source
// preserved at src/lib/_legacy/service-detail-content.legacy.ts.

import { readFileSync, writeFileSync, existsSync, mkdirSync, cpSync } from "node:fs";
import { dirname, join } from "node:path";

const ROOT = process.cwd();
const SRC = join(ROOT, "src/lib/service-detail-content.ts");
const LEGACY_DIR = join(ROOT, "src/lib/_legacy");
const LEGACY = join(LEGACY_DIR, "service-detail-content.legacy.ts");
const OUT_DIR = join(ROOT, "src/lib/services");

const CATEGORIES = {
  blockchain: [
    "blockchainDevelopment", "smartContract", "defiDevelopment",
    "web3DevelopmentCompany", "metaverseDevelopment", "dappDevelopment",
    "rwa", "blockchainConsultingServices", "layer1", "layer2",
  ],
  nft: ["nftMarketplaceDevelopment"],
  crypto: [
    "cryptoWalletDevelopment", "custodialWallet", "decentralizedExchange",
    "centralizedExchange", "icoDevelopment", "tokenDevelopment",
    "memeCoinDevelopment", "idoDevelopment",
  ],
  ai: [
    "aiConsultingServices", "aiAssistantDevelopment", "aiIntegration",
    "aiAutomation", "aiDevelopment", "generativeAiDevelopment", "chatGpt",
    "aiAgentDevelopment", "machineLearningDevelopment", "llmDevelopmentPartner",
    "computerVisionSoftwareDevelopment", "ragDevelopmentCompany",
    "rpaDevelopmentPartner", "aiVoiceAssist", "aiTool",
  ],
  "ai-vertical": [
    "aiBankingSolutions", "aiFintechSolutions", "aiLogisticsSoftware",
    "aiRealEstate", "aiHealthcareSoftware", "aiEducationSoftware",
  ],
  misc: ["aboutUs", "mobileAppDevelopment"],
};

// REGISTRY order — preserves original ordering exactly.
const REGISTRY_ORDER = [
  "blockchainDevelopment", "aiConsultingServices", "smartContract",
  "defiDevelopment", "web3DevelopmentCompany", "metaverseDevelopment",
  "nftMarketplaceDevelopment", "dappDevelopment", "rwa",
  "cryptoWalletDevelopment", "custodialWallet", "decentralizedExchange",
  "centralizedExchange", "icoDevelopment", "aiAssistantDevelopment",
  "aiIntegration", "aiAutomation", "aiDevelopment", "generativeAiDevelopment",
  "aiBankingSolutions", "aiFintechSolutions", "aiLogisticsSoftware",
  "aiRealEstate", "aiEducationSoftware", "aiHealthcareSoftware", "chatGpt",
  "aiAgentDevelopment", "machineLearningDevelopment", "llmDevelopmentPartner",
  "computerVisionSoftwareDevelopment", "ragDevelopmentCompany",
  "rpaDevelopmentPartner", "aiVoiceAssist", "aiTool",
  "blockchainConsultingServices", "layer1", "layer2", "tokenDevelopment",
  "memeCoinDevelopment", "idoDevelopment", "aboutUs", "mobileAppDevelopment",
];

const sourceFile = existsSync(LEGACY) ? LEGACY : SRC;
const src = readFileSync(sourceFile, "utf8");

if (!existsSync(LEGACY_DIR)) mkdirSync(LEGACY_DIR, { recursive: true });
if (!existsSync(LEGACY)) {
  cpSync(SRC, LEGACY);
  console.log(`Backed up original → ${LEGACY}`);
}

const lines = src.split("\n");

// Find each const block's [startLine, endLine].
const declRe = /^const\s+(\w+)\s*:\s*ServiceDetailContent\s*=\s*\{/;
const registryRe = /^const\s+REGISTRY\s*:/;

const decls = [];
let registryLineIdx = -1;
for (let i = 0; i < lines.length; i++) {
  const m = lines[i].match(declRe);
  if (m) decls.push({ name: m[1], startIdx: i });
  if (registryLineIdx === -1 && registryRe.test(lines[i])) registryLineIdx = i;
}

if (registryLineIdx === -1) {
  console.error("Could not find REGISTRY declaration");
  process.exit(1);
}

// End of each block = line right before the next const (or before REGISTRY for
// the final one). Trim trailing blank lines.
for (let i = 0; i < decls.length; i++) {
  const nextStart = i + 1 < decls.length ? decls[i + 1].startIdx : registryLineIdx;
  let endIdx = nextStart - 1;
  while (endIdx > decls[i].startIdx && lines[endIdx].trim() === "") endIdx--;
  decls[i].endIdx = endIdx;
}

const byName = new Map(decls.map((d) => [d.name, d]));

if (!existsSync(OUT_DIR)) mkdirSync(OUT_DIR, { recursive: true });

const HEADER = `// AUTO-GENERATED by scripts/split-registry.mjs.
// Source of truth: src/lib/_legacy/service-detail-content.legacy.ts.
// Edits to this file may be overwritten — edit the legacy file and re-run
// \`node scripts/split-registry.mjs\` to regenerate.

import type { ServiceDetailContent } from "../service-detail-types";

`;

const categoryFiles = {};
for (const [category, names] of Object.entries(CATEGORIES)) {
  const blocks = [];
  for (const name of names) {
    const d = byName.get(name);
    if (!d) {
      console.error(`Missing decl in source: ${name}`);
      process.exit(1);
    }
    const body = lines.slice(d.startIdx, d.endIdx + 1).join("\n");
    // Promote to export.
    blocks.push(body.replace(/^const\s+/, "export const "));
  }
  const filePath = join(OUT_DIR, `${category}.ts`);
  writeFileSync(filePath, HEADER + blocks.join("\n\n") + "\n");
  categoryFiles[category] = names;
  console.log(`Wrote ${filePath} — ${names.length} services`);
}

// Build the index.
const allImports = [];
for (const [category, names] of Object.entries(categoryFiles)) {
  allImports.push(
    `import { ${names.join(", ")} } from "./${category}";`
  );
}

const registryEntries = REGISTRY_ORDER.map(
  (n) => `  [${n}.slug]: ${n},`
).join("\n");

const indexContent = `// AUTO-GENERATED by scripts/split-registry.mjs.
// Service-detail content registry. Public API preserved exactly:
//   - getServiceDetailContent(slug)
//   - listServiceDetailSlugs()
//
// To edit a service, edit src/lib/_legacy/service-detail-content.legacy.ts
// (the source of truth) and re-run \`node scripts/split-registry.mjs\`.

import type { ServiceDetailContent } from "../service-detail-types";
${allImports.join("\n")}

const REGISTRY: Record<string, ServiceDetailContent> = {
${registryEntries}
};

// Build-time sanity checks — catch slug/key drift before the bundler does.
for (const [key, svc] of Object.entries(REGISTRY)) {
  if (svc.slug !== key) {
    throw new Error(
      \`[services] Slug mismatch — registry key "\${key}" but service.slug "\${svc.slug}"\`
    );
  }
}

export function getServiceDetailContent(slug: string): ServiceDetailContent | null {
  return REGISTRY[slug] ?? null;
}

export function listServiceDetailSlugs(): string[] {
  return Object.keys(REGISTRY);
}
`;

writeFileSync(join(OUT_DIR, "index.ts"), indexContent);
console.log(`Wrote ${join(OUT_DIR, "index.ts")}`);

// Replace the original file with a thin re-export so existing imports keep working.
const reexport = `// Thin re-export. The service registry was split into src/lib/services/ in
// May 2026. Public surface preserved for backwards compatibility.
export { getServiceDetailContent, listServiceDetailSlugs } from "./services";
`;
writeFileSync(SRC, reexport);
console.log(`Replaced ${SRC} with thin re-export`);

// Sanity: confirm every name was extracted.
const known = new Set(Object.values(CATEGORIES).flat());
const missing = decls.filter((d) => !known.has(d.name));
if (missing.length) {
  console.warn("Decls not assigned to any category:", missing.map((d) => d.name));
}
console.log(`Done. Split ${decls.length} services into ${Object.keys(CATEGORIES).length} files.`);
